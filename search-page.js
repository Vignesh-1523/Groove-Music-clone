function getSong(index) {
    duplicateIndex.push(index);
    if(duplicateIndex.length > 1){
        if(duplicateIndex[0] == duplicateIndex[1]){ 
            duplicateIndex.shift();
        } else{
            duplicateIndex.shift();
            newListCreation();
        }
    } else{
        newListCreation();
    }
    function newListCreation() {
       
        let audioSource = song_Details[index][4];
        let  songImage = song_Details[index][5];
        console.log(songImage);

        list = document.createElement('div');

        for (let i = 0; i < 4; i++) {
          list.appendChild(document.createElement('span'));
        }
    
        let audioFile = document.createElement('audio');
        list.appendChild(audioFile);
        audioFile.src = audioSource;
    
        let imageFile = document.createElement('img');
        imageFile.style.display = "none";
        list.appendChild(imageFile);
        imageFile.src = songImage;
    
        // here we select all the four span elements created inside the div element..
        let spanElements = list.querySelectorAll('span');
        let x = 0;
        spanElements.forEach(span => {
          span.innerHTML = song_Details[index][x];
          x++;
        });
        
        searchedSongs.appendChild(list);
        count.push(list); // this push list that is created. So only filtered non-duplicated songs are created as list hence they are pushed.
}
}
